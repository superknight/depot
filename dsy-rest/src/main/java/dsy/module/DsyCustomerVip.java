package dsy.module;

public class DsyCustomerVip {

	private String id;
	private String customer;
	private String vip_grade;
	private String vip_integral;
	private String status;
	private String apply_time;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCustomer() {
		return customer;
	}
	public void setCustomer(String customer) {
		this.customer = customer;
	}
	public String getVip_grade() {
		return vip_grade;
	}
	public void setVip_grade(String vip_grade) {
		this.vip_grade = vip_grade;
	}
	public String getVip_integral() {
		return vip_integral;
	}
	public void setVip_integral(String vip_integral) {
		this.vip_integral = vip_integral;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getApply_time() {
		return apply_time;
	}
	public void setApply_time(String apply_time) {
		this.apply_time = apply_time;
	}
	
	
}
